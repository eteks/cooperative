						<li>Lumpsum grant to the family members of the  deceased weaver member : Rs. 10,000/- each.</li>
						<li>Mahatma Gandhi Bunkar Bima Yojana Insurance  Scheme : The scheme is administrated by the LIC of India.</li>
					
					<ul class="list">
						<li>In the event of death of the member, sum  assured of Rs. 60,000/- will become payable to the nominee.</li>
