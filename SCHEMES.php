 					<ul class="list">
						<li>Pre-natal and post - natal subsidy of women  Rs. 1,500/- each.</li>
						<li>Rainy season subsidy to be paid during rainy  season i.e. during the month of October - November : Rs. 2,000/- per weaver  (Rs. 1,500/- from Weaver Welfare Fund and Rs. 500/- from their own contribution).</li>
						<li>Pension And Other Welfare Schemes : Rs. 500/-  per month for those whose average monthly wage is Rs. 100/- or more but below  Rs. 300/- per month during the last three years, and Rs. 1000/- per month to  those whose average monthly wages is not less than Rs. 300/- during the last  three years. If a member dies before attaining the age of 60, family pension  will be paid to the legal heir of the deceased.</li>
						<li>Assistance for family planning operation : Rs.  200/- per weaver.</li>
						<li>Assistance for purchase of spectacles : Rs.  300/- per weaver (Once in three years).</li>
						<li>Free dhoties and sarees to weaver - member. (Once  in a year, two sets each).</li>
						<li>Medical allowance to the handloom weavers :  Rs. 100/- per month provided the weaver has worked for 180 days or has earned  Rs. 2,500/- as wage during the preceding year.</li>
						<li>Subsidy to purchase a plot : A sum of Rs.  10,000/- per weaver is paid as grant for purchase of a plot.</li>
						<li>Thrift fund scheme : 12% Government matching  contribution is granted on the wages earned by a weaver member.</li>
						<li>Incentive on wages :  25% wage incentive subject to a maximum of  Rs. 5,000/- is paid to the weaver members on the wage earned by them during  &quot;Deepavali Festival &quot;</li>
						<li>Weaving appliance subsidy : In order to  purchase weaving appliances, a weaver member is given 25% loan and 75% subsidy.</li>
						<li>E.S.I. scheme :  E.S.I. coverage is provided to the in - shed  weaver members of the primary weavers' co-operative societies.</li>
						<li>Identity cards to weavers : Identity Cards are  issued to the handloom weavers working in the primary weavers' co-operative  societies functioning in the Union territory of Puducherry.</li>
						<li>Integrated handloom training project : Every  year, training is imparted to the handloom weavers in the area of weaving,  dyeing and designing through Weavers Service Centre, Chennai</li>
